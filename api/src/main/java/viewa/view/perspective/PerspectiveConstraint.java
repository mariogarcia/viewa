package viewa.view.perspective;

/**
 * This enumeration type represents all possible constraints in a given perspective
 * 
 * @author mario
 * @since 1.0.2
 */
public enum PerspectiveConstraint {
	LEFT,
	LEFT_TOP,
	LEFT_MIDDLE,
	LEFT_BOTTOM,
	BOTTOM, 
	RIGHT,
	RIGHT_TOP,
	RIGHT_MIDDLE,
	RIGHT_BOTTOM,
	TOP 	
}
